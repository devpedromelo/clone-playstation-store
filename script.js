function gridObject(g){
    let spanItems = g.map(function(item){
        return `<li class="grid-list-collection">
                    <a href="@">
                        <div class="psw-line-center">
                            <span><img src="${item.img}"></span>
                            <div class="hover"></div>
                        </div>
                    </a>
                </li>`
    });
    spanItems = spanItems.join(" ");
    span.innerHTML = spanItems;
};

